ALTER TABLE stamps ENABLE ROW LEVEL SECURITY;
ALTER TABLE charm_tags ENABLE ROW LEVEL SECURITY;
ALTER TABLE stamp_charm_tags ENABLE ROW LEVEL SECURITY;
ALTER TABLE triage_sessions ENABLE ROW LEVEL SECURITY;
ALTER TABLE triage_results ENABLE ROW LEVEL SECURITY;

-- Stamps: users can CRUD their own
CREATE POLICY "Users can view own stamps" ON stamps FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert own stamps" ON stamps FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "Users can delete own stamps" ON stamps FOR DELETE USING (auth.uid() = user_id);

-- Charm tags: users can see system tags + own tags
CREATE POLICY "Users can view system and own tags" ON charm_tags FOR SELECT
  USING (is_system = true OR auth.uid() = user_id);
CREATE POLICY "Users can insert own tags" ON charm_tags FOR INSERT
  WITH CHECK (auth.uid() = user_id AND is_system = false);
CREATE POLICY "Users can delete own tags" ON charm_tags FOR DELETE
  USING (auth.uid() = user_id AND is_system = false);

-- Stamp charm tags: users can manage their own stamp's tags
CREATE POLICY "Users can view own stamp tags" ON stamp_charm_tags FOR SELECT
  USING (EXISTS (SELECT 1 FROM stamps WHERE stamps.id = stamp_id AND stamps.user_id = auth.uid()));
CREATE POLICY "Users can insert own stamp tags" ON stamp_charm_tags FOR INSERT
  WITH CHECK (EXISTS (SELECT 1 FROM stamps WHERE stamps.id = stamp_id AND stamps.user_id = auth.uid()));
CREATE POLICY "Users can delete own stamp tags" ON stamp_charm_tags FOR DELETE
  USING (EXISTS (SELECT 1 FROM stamps WHERE stamps.id = stamp_id AND stamps.user_id = auth.uid()));

-- Triage sessions: users can CRUD their own
CREATE POLICY "Users can view own triage sessions" ON triage_sessions FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert own triage sessions" ON triage_sessions FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "Users can update own triage sessions" ON triage_sessions FOR UPDATE USING (auth.uid() = user_id);

-- Triage results: users can manage via their own sessions
CREATE POLICY "Users can view own triage results" ON triage_results FOR SELECT
  USING (EXISTS (SELECT 1 FROM triage_sessions WHERE triage_sessions.id = session_id AND triage_sessions.user_id = auth.uid()));
CREATE POLICY "Users can insert own triage results" ON triage_results FOR INSERT
  WITH CHECK (EXISTS (SELECT 1 FROM triage_sessions WHERE triage_sessions.id = session_id AND triage_sessions.user_id = auth.uid()));

